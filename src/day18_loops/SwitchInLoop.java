package day18_loops;

public class SwitchInLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 5 ; i++) {

            if (i ==0) {
                System.out.println("Zero");
            } else if (i ==1) {
                System.out.println("One");
            }else if (i==2) {
                System.out.println("Two");
            }
            System.out.println("_____________");

            ;

        }
    }
}
