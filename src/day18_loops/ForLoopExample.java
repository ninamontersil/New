package day18_loops;

public class ForLoopExample {
    public static void main(String[] args) {
        //for loop

        System.out.println("This is \"for\" loop; ");


        for (int i = 0; i <= 10; i++) {
           // System.out.println(i); since it was declared inside of for loop, not accesible out side of for loop

        }
        /* flow 1/ initialization. 2. check condition.
        2.a. if true - execute the code block
        3. - iteration
        4 - repeat from 2
        2b. - false - end the loop

         */
        System.out.println();



    }
}
